"""
main.py - Демонстрация на Библиотечния мениджър
"""

from models import Library, Book


def main():
    # Създаване на библиотека
    lib = Library("Градска библиотека — София")

    print("\n" + "="*50)
    print("  📚 БИБЛИОТЕЧЕН МЕНИДЖЪР  ")
    print("="*50 + "\n")

    # Добавяне на книги
    books_data = [
        Book("Под игото",           "Иван Вазов",          1894, "Роман",         3),
        Book("Хан Аспарух",         "Антон Дончев",        1972, "Исторически",   2),
        Book("Тютюн",               "Димитър Димов",       1951, "Роман",         2),
        Book("Фараонът",            "Болеслав Прус",       1895, "Исторически",   1),
        Book("Майстора и Маргарита","Михаил Булгаков",     1967, "Фантастика",    2),
        Book("Дон Кихот",           "Мигел де Сервантес", 1605, "Приключенски",  1),
        Book("1984",                "Джордж Оруел",        1949, "Дистопия",      3),
        Book("Малкият принц",       "Антоан дьо Сент-Екзюпери", 1943, "Философска", 4),
        Book("Процесът",            "Франц Кафка",         1925, "Философска",    2),
        Book("Красавицата и звяра", "Вилньов",             1740, "Приказка",      1),
    ]

    print("Зареждане на книги")
    for book in books_data:
        lib.add_book(book)

    # ── Списък, сортиран по автор
    lib.list_books(sort_by="author")

    # Заемания
    print("Заемания")
    lib.borrow_book("1984",       "Мария Иванова",  days=10)
    lib.borrow_book("Под игото", "Петър Петров",   days=14)
    lib.borrow_book("Под игото", "Стела Георгиева",days=7)
    lib.borrow_book("Под игото", "Никола Стоянов", days=21) 
    lib.borrow_book("Под игото", "Още един читател")        
    lib.borrow_book("Непозната книга", "Кой да е")        

    # Търсене 
    print("\nТърсене")
    lib.search("Исторически")    # по жанр
    lib.search("Димов")          # по автор
    lib.search("xyz")            # без резултат

    # Списък само с налични (сортирани по година)
    lib.list_books(sort_by="year", only_available=True)

    # Статистика
    lib.statistics()

    # Връщане
    print("Връщания")
    lib.return_book("1984",       "Мария Иванова")
    lib.return_book("Под игото", "Петър Петров")
    lib.return_book("Тютюн",     "Никой")         # грешка — не е заета

    # Финална статистика ────────────────────────────────────────────────
    lib.statistics()


if __name__ == "__main__":
    main()